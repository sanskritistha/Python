#using remove() method:
nums=[1,2,3,4,4,2]
nums.remove(2)#remove first occurence
print(nums)

#using pop() method
alpha=['a','b','c','d']
alpha.pop(1)
print(alpha)

#using del keyword
alpha=['a','b','c','d']
del alpha[1]
print(alpha)

#using clear method
nums=[1,2,3]
nums.clear()
print(nums)
